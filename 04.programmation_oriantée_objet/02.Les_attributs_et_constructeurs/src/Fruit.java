public class Fruit {
    public String Nom;
    public String Couleur;
    public String Origine;

    public Fruit(String Nom, String Couleur, String Origine) {
        this.Nom = Nom;
        this.Couleur = Couleur;
        this.Origine = Origine;
    }

    public Fruit(String Nom) {
        this.Nom = Nom;
    }


}
